def append_memory_entry(
    *,
    review_date: date,
    analysis: str,
    proposal: StrategyProposal | None,
    outcome: str,
    sandbox_details: str = "",
    grid_search_summary: str = "",
) -> None:
    """Append one monthly entry to MEMORY.md.

    Called by monthly_review.py AFTER the sandbox decision is known so the
    entry captures both what the analyst proposed AND what actually happened.

    Parameters
    ----------
    review_date    : ISO date of the monthly review
    analysis       : the analyst's qualitative narrative for this month
    proposal       : the StrategyProposal (or None if no proposal)
    outcome        : one of "accepted", "rejected", "no_proposal", "error"
    sandbox_details: sandbox metrics + rejection reason (if any)
    grid_search_summary: brief summary of the math grid search step
    """
    _ANALYST_DIR.mkdir(parents=True, exist_ok=True)

    proposal_block = (
        f"- **name**: `{proposal.name}`\n"
        f"- **class**: `{proposal.class_name}`\n"
        f"- **reasoning**:\n\n{proposal.reasoning}\n"
    ) if proposal else "_No strategy proposed this month._"

    entry = (
        f"\n### {review_date.isoformat()} — Monthly Review\n\n"
        f"**Grid-search step**: {grid_search_summary or '(none)'}\n\n"
        f"**Outcome**: `{outcome}`\n\n"
        f"**Sandbox result**: {sandbox_details or '(N/A)'}\n\n"
        f"**Analysis**:\n\n{analysis}\n\n"
        f"**Proposal**:\n\n{proposal_block}\n\n"
        f"---\n"
    )

    # Insert just above the insertion marker so newest stays near the bottom
    # (the marker comment is at the file's end, before "_End of MEMORY.md_").
    current = _MEMORY_MD.read_text() if _MEMORY_MD.exists() else (
        "# MEMORY.md\n\n<!-- ENTRIES_INSERTION_POINT -->\n\n_End of MEMORY.md_\n"
    )
    marker = "<!-- ENTRIES_INSERTION_POINT"
    if marker in current:
        before, after = current.split(marker, 1)
        # 'after' starts with the rest of the marker line; preserve it
        marker_line_end = after.find("\n")
        marker_line = marker + after[: marker_line_end + 1]
        rest = after[marker_line_end + 1 :]
        new_content = before + entry + marker_line + rest
    else:
        # Fallback: append at end
        new_content = current.rstrip() + "\n" + entry
    _MEMORY_MD.write_text(new_content)
    logger.info("ai_analyst: appended %s entry to MEMORY.md", review_date.isoformat())



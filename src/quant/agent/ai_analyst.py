class AIAnalyst:
    """Calls the Anthropic API to analyze trading results and generate strategies.

    Usage::

        analyst = AIAnalyst()
        report = analyst.analyze(
            daily_runs=runs,
            current_state={"sma_fast": 50, ...},
            ai_strategy_names=["rsi_reversal_14_30"],
            grid_search_summary="No change: no candidate beat current.",
        )
        print(report.analysis)
        if report.proposed_strategy:
            print(report.proposed_strategy.code)

    Raises ``RuntimeError`` if ``ANTHROPIC_API_KEY`` is not set.
    Raises ``ImportError`` if ``anthropic`` is not installed.
    """

    def __init__(self, model: str = _MODEL) -> None:
        # override=True ensures the .env value wins even if a blank
        # ANTHROPIC_API_KEY was already set in the shell environment.
        try:
            from dotenv import load_dotenv as _load  # noqa: PLC0415
            _load(override=True)
        except ImportError:
            pass
        api_key = os.environ.get("ANTHROPIC_API_KEY")
        if not api_key:
            raise RuntimeError(
                "ANTHROPIC_API_KEY not set in .env. "
                "Get a key from console.anthropic.com and add:\n"
                "  ANTHROPIC_API_KEY=sk-ant-...\n"
                "to your .env file."
            )
        try:
            import anthropic  # noqa: PLC0415
        except ImportError as exc:
            raise ImportError(
                "anthropic package not installed. Run: uv sync"
            ) from exc
        self._client = anthropic.Anthropic(api_key=api_key)  # type: ignore[attr-defined]
        self._model = model

    def _create_message(self, **kwargs: Any):
        """``messages.create`` with retries tuned for the VPN deployment.

        The SDK's built-in retries cover 429/5xx but NOT 403 — and on
        this operator's network a 403 is almost never a real permission
        problem: it means the VPN exit IP landed on Anthropic's proxy/
        datacenter blocklist (see ``_describe_ai_failure``). Exit IPs
        rotate per connection, so a fresh attempt after a pause usually
        succeeds — the 2026-06-06 weekly review died on a single 403
        that a retry would likely have cleared. Long backoffs are fine:
        every caller is an unattended batch job.

        Connection errors (tunnel re-establishing, GFW interference)
        retry for the same reason. Genuine auth failures raise
        ``AuthenticationError`` (401), which is NOT retried.
        """
        import anthropic  # noqa: PLC0415

        from quant.util.retry import retry_on_transient  # noqa: PLC0415

        return retry_on_transient(
            lambda: self._client.messages.create(**kwargs),
            transient=(
                anthropic.PermissionDeniedError,   # 403 — blocklisted VPN exit IP
                anthropic.APIConnectionError,      # tunnel drop / GFW reset
            ),
            description="Anthropic messages.create",
            backoffs=(30.0, 60.0, 120.0),
        )

    def analyze(
        self,
        *,
        daily_runs: list[dict[str, Any]],
        current_state: dict[str, Any],
        ai_strategy_names: list[str] | None = None,
        grid_search_summary: str = "Grid search was not run this month.",
        recent_weekly_reports: list[dict[str, Any]] | None = None,
        monthly_metrics: dict[str, Any] | None = None,
        pipeline_snapshot: dict[str, Any] | None = None,
        quant_diagnostics: dict[str, Any] | None = None,
    ) -> AnalysisReport:
        """Call Claude to analyze results and optionally propose a new strategy.

        Parameters
        ----------
        daily_runs
            List of daily run payloads loaded via ``load_daily_run``.
        current_state
            ``EnsembleState`` serialized as a dict (via ``dataclasses.asdict``).
        ai_strategy_names
            Names of AI strategies already active in the ensemble (to avoid
            proposing duplicates).
        grid_search_summary
            Human-readable string from the monthly grid search step.
        recent_weekly_reports
            Past ~4 weekly narratives (oldest first) so the monthly analyst
            BUILDS ON the weekly analyst's curated observations instead of
            re-deriving attribution from raw daily data. Each is a dict
            with at least ``week_ending`` and ``narrative`` keys. Watch for
            ``ESCALATION_MARKER`` strings from the weekly side.

        Returns
        -------
        AnalysisReport
            Always contains ``analysis`` text. ``proposed_strategy`` is
            ``None`` when Claude decides no new strategy is needed.
        """
        user_msg = _build_user_message(
            daily_runs,
            current_state,
            grid_search_summary,
            ai_strategy_names or [],
            recent_weekly_reports=recent_weekly_reports,
            monthly_metrics=monthly_metrics,
            pipeline_snapshot=pipeline_snapshot,
            quant_diagnostics=quant_diagnostics,
        )
        logger.info(
            "ai_analyst: calling %s with %d daily runs", self._model, len(daily_runs)
        )

        # Rebuild the system prompt EACH call so MEMORY.md and STRATEGY_LIBRARY.md
        # updates from prior months are immediately visible.
        system_prompt = _build_system_prompt()
        message = self._create_message(
            model=self._model,
            max_tokens=8192,
            system=system_prompt,
            messages=[{"role": "user", "content": user_msg}],
        )
        raw = message.content[0].text.strip()
        logger.debug("ai_analyst raw response (first 500 chars): %s", raw[:500])

        data = _parse_json_response(raw)

        analysis_text = data.get("analysis", "(No analysis returned.)")
        proposal_data = data.get("proposed_strategy")

        proposal: StrategyProposal | None = None
        if proposal_data and isinstance(proposal_data, dict):
            try:
                proposal = StrategyProposal(
                    name=proposal_data["name"],
                    class_name=proposal_data["class_name"],
                    reasoning=proposal_data.get("reasoning", ""),
                    code=proposal_data["code"],
                )
            except KeyError as e:
                logger.warning(
                    "ai_analyst: proposed_strategy missing field %s — ignoring proposal", e
                )

        # Parse the optional state-change proposal (e.g., new trail_pct).
        # Validation here is light — type-coerce and clamp. The actual
        # gate (trail_pct ≤ STOP_LOSS_PCT) lives in monthly_review where
        # the proposal is surfaced to the operator.
        state_change_data = data.get("proposed_state_changes")
        state_change: StateChangeProposal | None = None
        if state_change_data and isinstance(state_change_data, dict):
            def _opt_float(key: str) -> float | None:
                raw = state_change_data.get(key)
                return float(raw) if raw is not None else None

            def _opt_int(key: str) -> int | None:
                raw = state_change_data.get(key)
                return int(raw) if raw is not None else None

            try:
                state_change = StateChangeProposal(
                    trail_pct=_opt_float("trail_pct"),
                    sma_fast=_opt_int("sma_fast"),
                    sma_slow=_opt_int("sma_slow"),
                    mr_lookback=_opt_int("mr_lookback"),
                    mr_threshold_pct=_opt_float("mr_threshold_pct"),
                    reasoning=str(state_change_data.get("reasoning", "")),
                )
            except (TypeError, ValueError) as e:
                logger.warning(
                    "ai_analyst: proposed_state_changes parse error: %s — ignoring",
                    e,
                )

        # Parse pipeline self-audit findings — list of {severity, category,
        # description, recommendation}. Empty list is the common "all clear"
        # case. Malformed entries are dropped with a warning.
        findings_raw = data.get("pipeline_findings") or []
        findings: list[PipelineFinding] = []
        if isinstance(findings_raw, list):
            for entry in findings_raw:
                if not isinstance(entry, dict):
                    continue
                try:
                    findings.append(PipelineFinding(
                        severity=str(entry.get("severity", "low")),
                        category=str(entry.get("category", "other")),
                        description=str(entry.get("description", "")),
                        recommendation=str(entry.get("recommendation", "")),
                    ))
                except (TypeError, ValueError) as e:
                    logger.warning(
                        "ai_analyst: pipeline_findings entry parse error: %s — skipping",
                        e,
                    )

        return AnalysisReport(
            analysis=analysis_text,
            proposed_strategy=proposal,
            proposed_state_changes=state_change,
            pipeline_findings=findings,
        )

    def analyze_weekly(
        self,
        *,
        daily_runs: list[dict[str, Any]],
        weekly_metrics: dict[str, Any],
        hrp_diagnostic: dict[str, Any] | None = None,
        past_weekly_reports: list[dict[str, Any]] | None = None,
    ) -> WeeklyAnalysisReport:
        """Call Claude to write a deep-dive analysis of the past week.

        Returns a markdown narrative the weekly review email embeds verbatim.
        Does NOT propose strategies or state changes — that's the monthly
        analyst's job (see WEEKLY_ANALYST.md §3).

        Parameters
        ----------
        daily_runs
            Daily run payloads from the past week (load_daily_run).
        weekly_metrics
            Pre-computed performance metrics for the week — return, Sharpe,
            max DD, win rate, attribution, concentration. See
            weekly_review._compute_weekly_metrics for the canonical builder.
        hrp_diagnostic
            Output of refit_hrp_weights' diag dict (per-strategy backtest
            stats, weight before/after). Helps the analyst reason about
            which strategies are gaining or losing weight and why.
        """
        runs_table = _summarise_runs(daily_runs)
        metrics_json = json.dumps(weekly_metrics, indent=2, default=str)
        hrp_json = json.dumps(hrp_diagnostic or {}, indent=2, default=str)

        # Past weekly reports — oldest first — for self-improvement per
        # WEEKLY_ANALYST.md §5. Same renderer the monthly call uses; only
        # the framing copy differs.
        past_section = _render_past_weeklies(
            past_weekly_reports or [],
            heading=(
                "Recent weekly reports — your own past narratives "
                "(oldest first) — see WEEKLY_ANALYST.md §5"
            ),
            empty_text=(
                "No past weekly reports yet — this is one of your first calls. "
                "Establish the baseline; future weeks will reference back to "
                "what you observe here."
            ),
        )

        user_msg = (
            f"## Past week's daily runs\n\n{runs_table}\n\n"
            f"## Pre-computed weekly metrics\n\n```json\n{metrics_json}\n```\n\n"
            f"## HRP refit diagnostic (this Saturday's refit)\n\n```json\n{hrp_json}\n```\n\n"
            f"{past_section}"
            "---\n\nWrite the weekly performance deep-dive per "
            "WEEKLY_ANALYST.md §2. Markdown only, no JSON, no fences. "
            "Begin with the headline paragraph; end with the watch items. "
            "If past reports exist, apply WEEKLY_ANALYST.md §5 — note "
            "continuity, self-critique, escalations."
        )
        logger.info(
            "ai_analyst: weekly call with %d daily runs", len(daily_runs)
        )

        system_prompt = _build_weekly_system_prompt()
        message = self._create_message(
            model=self._model,
            max_tokens=4096,
            system=system_prompt,
            messages=[{"role": "user", "content": user_msg}],
        )
        narrative = message.content[0].text.strip()
        # Strip accidental code fences if the model wrapped the output.
        if narrative.startswith("```"):
            lines = narrative.splitlines()
            # Drop opening fence + optional language tag.
            if lines and lines[0].startswith("```"):
                lines = lines[1:]
            # Drop closing fence.
            if lines and lines[-1].startswith("```"):
                lines = lines[:-1]
            narrative = "\n".join(lines).strip()

        return WeeklyAnalysisReport(narrative=narrative)



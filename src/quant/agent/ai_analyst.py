        f"- Max drawdown: {max_drawdown:.1%}\n"
        f"- DSR: {dsr:.3f}\n\n"
        f"**Reasoning at acceptance**:\n\n{proposal.reasoning}\n\n"
        f"**Code at acceptance**:\n\n"
        f"```python\n{proposal.code}\n```\n\n"
        f"---\n"

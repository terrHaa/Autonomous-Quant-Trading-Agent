"""ai_analyst.py — Claude-powered monthly strategy analyst and code generator.

Each monthly review calls ``AIAnalyst.analyze()`` once. It receives the
month's trading results, the current EnsembleState, and a summary of the
grid-search step. It returns:

  - A qualitative narrative: what worked, what didn't, observed patterns.
  - Optionally, a new strategy proposal: a complete Python class that
    implements the Strategy Protocol (``name`` + ``on_bar``).

The proposal is then handed to ``strategy_sandbox.py`` for three layers of
validation before anything touches the ensemble. This module's only job is
calling the Anthropic API and parsing the response — all gate logic is
elsewhere.

Cost estimate
-------------
~$0.30–0.50 per monthly call using claude-opus-4-5. Runs exactly once per
month, so the annual AI cost for this feature is ~$4. The ANTHROPIC_API_KEY
must be set in ``.env``; the module raises ``RuntimeError`` otherwise so
the failure is obvious rather than silent.
"""

from __future__ import annotations

import json
import logging
import os
import re
from dataclasses import dataclass, field
from datetime import date
from pathlib import Path
from typing import Any

logger = logging.getLogger(__name__)

# Model choice: Opus-class reasoning for code generation that runs autonomously
# on real capital. The cost premium (~$0.30/call) is trivial vs the downside of
# a buggy strategy generated by a cheaper model.
_MODEL = "claude-opus-4-5"

# Cross-system contract. The weekly analyst emits this marker on observations
# that have persisted ≥2 weeks; the monthly analyst scans for it as PRIMARY
# input to its proposals. Hardcoded as a constant here so a typo can't
# silently break the escalation pipeline. The .md prompts are read into the
# system prompt, so they reference the same literal — but only the analysts
# write it; Python code that READS for it must use this constant.
ESCALATION_MARKER = "WORTH ESCALATING TO MONTHLY REVIEW"

# Persistent-context files — the analyst's "brain". See ai-analyst/*.md docs.
# Project root: ai_analyst.py is at src/quant/agent/ai_analyst.py → 4 parents up.
_PROJECT_ROOT = Path(__file__).resolve().parent.parent.parent.parent
_ANALYST_DIR = _PROJECT_ROOT / "ai-analyst"
_ANALYST_MD = _ANALYST_DIR / "ANALYST.md"
_STRATEGY_LIBRARY_MD = _ANALYST_DIR / "STRATEGY_LIBRARY.md"
_EDGE_TAXONOMY_MD = _ANALYST_DIR / "EDGE_TAXONOMY.md"
_ANTI_PATTERNS_MD = _ANALYST_DIR / "ANTI_PATTERNS.md"
_MEMORY_MD = _ANALYST_DIR / "MEMORY.md"
_WEEKLY_ANALYST_MD = _ANALYST_DIR / "WEEKLY_ANALYST.md"

# The system prompt is BUILT at call time by concatenating:
#   1. ANALYST.md         → constitution (identity, standards, methodology)
#   2. STRATEGY_LIBRARY.md → canonical catalog of CURRENT active strategies
#                           + the trail_pct knob and operator hard rules
#   3. EDGE_TAXONOMY.md   → map of POSSIBLE strategy categories with
#                           coverage map (what's filled, what's a real gap)
#   4. ANTI_PATTERNS.md   → failure modes to check against before proposing
#   5. MEMORY.md          → append-only log of past proposals and outcomes
#   6. _RESPONSE_SHAPE    → the strict JSON schema the analyst must return
#
# This puts the analyst's persistent "brain" in front of every monthly call.
# The five .md files give it identity (1), current state (2), the space of
# possibilities (3), known failure modes (4), and history (5);
# the response shape ensures the output is mechanically parseable.

_RESPONSE_SHAPE = """\
════════════════════════════════════
RESPONSE FORMAT — raw JSON ONLY, no markdown fences
════════════════════════════════════
Return a single JSON object with this exact shape:

{
  "analysis": "<3–5 paragraph quantitative analysis. See ANALYST.md §6 for required workflow.>",
  "proposed_strategy": {
    "name": "<snake_case_with_params e.g. rsi_reversal_14_30>",
    "class_name": "<PascalCase e.g. RsiReversal>",
    "reasoning": "<Must include all 6 sections from ANALYST.md §2: edge thesis, math spec, regime analysis table, correlation hypothesis, statistical properties, falsification criteria>",
    "code": "<complete self-contained Python class string — see ANALYST.md §3>"
  } | null,
  "proposed_state_changes": {
    "trail_pct": <float in (0, 0.05] | null>,
    "sma_fast": <int in [10, 100] | null>,
    "sma_slow": <int in [50, 300] | null>,
    "mr_lookback": <int in [2, 30] | null>,
    "mr_threshold_pct": <float in (0.001, 0.1) | null>,
    "reasoning": "<quantitative evidence per ANALYST.md §6.5 — cite specific symbols, give-back magnitudes, or whipsaw counts from the daily runs table. Vague intuition is NOT acceptable.>"
  } | null,
  "pipeline_findings": [
    {
      "severity": "critical" | "high" | "medium" | "low",
      "category": "drift" | "dead_code" | "missing_safeguard" | "industry_norm",
      "description": "<1-2 sentences: what's wrong, with the specific numbers>",
      "recommendation": "<1-2 sentences: what to do about it>"
    },
    ...
  ]
}

ALL of the following are acceptable structures:
  - Strategy proposal AND state change   (both fields populated)
  - Strategy proposal only               (proposed_state_changes: null)
  - State change only                    (proposed_strategy: null)
  - Analysis only                        (both null — per ANALYST.md §7)
  - pipeline_findings: ALWAYS present (empty list if nothing found)

Use `proposed_strategy` for proposing a NEW strategy class.
Use `proposed_state_changes` for tuning the trailing-stop distance.
Use `pipeline_findings` for INFRASTRUCTURE issues you spotted in the
  monthly self-audit (config drift, dead code, missing safeguards,
  thresholds below institutional norms). See ANALYST.md §6 step 5c.
Use `analysis` text for everything else (qualitative commentary,
  regime observations, ensemble-mix opinions, etc.).
"""


def _read_file(path: Path) -> str:
    """Read a context file. Returns a placeholder if missing — never raises."""
    try:
        return path.read_text()
    except FileNotFoundError:
        logger.warning("ai_analyst: context file missing — %s", path)
        return f"(File {path.name} not found — operator should create it.)"


def _build_system_prompt() -> str:
    """Concatenate the five persistent .md files + response shape."""
    analyst_md = _read_file(_ANALYST_MD)
    library_md = _read_file(_STRATEGY_LIBRARY_MD)
    edge_md = _read_file(_EDGE_TAXONOMY_MD)
    anti_md = _read_file(_ANTI_PATTERNS_MD)
    memory_md = _read_file(_MEMORY_MD)
    return (
        f"# === ANALYST.md (your constitution) ===\n\n{analyst_md}\n\n"
        f"# === STRATEGY_LIBRARY.md (active strategies + tunable knobs) ===\n\n"
        f"{library_md}\n\n"
        f"# === EDGE_TAXONOMY.md (the space of possible edges + coverage map) ===\n\n"
        f"{edge_md}\n\n"
        f"# === ANTI_PATTERNS.md (failure modes — check against before proposing) ===\n\n"
        f"{anti_md}\n\n"
        f"# === MEMORY.md (your own past proposals + outcomes) ===\n\n{memory_md}\n\n"
        f"# === RESPONSE PROTOCOL ===\n\n{_RESPONSE_SHAPE}"
    )


def _build_weekly_system_prompt() -> str:
    """Build the WEEKLY analyst's system prompt — narrower than monthly.

    Weekly mode does NOT propose strategies or state changes; it writes a
    performance deep-dive. The prompt loads only the files the weekly
    analyst actually needs (saving ~7k tokens / ~$0.10/week vs reusing
    the monthly system prompt verbatim).
    """
    weekly_md = _read_file(_WEEKLY_ANALYST_MD)
    library_md = _read_file(_STRATEGY_LIBRARY_MD)
    memory_md = _read_file(_MEMORY_MD)
    return (
        f"# === WEEKLY_ANALYST.md (your role for this call) ===\n\n{weekly_md}\n\n"
        f"# === STRATEGY_LIBRARY.md (the ensemble you are analyzing) ===\n\n"
        f"{library_md}\n\n"
        f"# === MEMORY.md (history from the monthly analyst — context only) ===\n\n"
        f"{memory_md}\n\n"
        "# === RESPONSE PROTOCOL ===\n\n"
        "Return raw markdown ONLY — no JSON, no fences. The markdown will be "
        "embedded directly into the weekly review email. Follow the paragraph "
        "structure prescribed in WEEKLY_ANALYST.md §2. No preamble. No "
        "sign-off. Begin with the headline paragraph and end with the watch "
        "items.\n"
    )



@dataclass
class StrategyProposal:
    """A proposed new strategy from the AI analyst."""
    name: str         # snake_case identifier used as the file name on disk
    class_name: str   # PascalCase — the class name inside the code
    reasoning: str    # human-readable explanation of the edge
    code: str         # complete Python source for the strategy class


@dataclass
class StateChangeProposal:
    """A proposed adjustment to a tunable EnsembleState field.

    Currently the only knob the analyst controls is ``trail_pct`` (the
    trailing-stop distance, see ANALYST.md §6.5). Future tunables can
    be added as optional fields — old months' MEMORY.md entries will
    simply have them absent.
    """
    trail_pct: float | None = None   # new trailing-stop distance, in (0, 0.05]
    # T4.22 — SMA + MR knobs are now tunable via the analyst's structured
    # channel. Each proposal MAY include any subset of these; None means
    # "no change to that knob". The operator-side apply path validates
    # the values before writing to EnsembleState.
    sma_fast: int | None = None      # new SMA fast window (typical 20-100)
    sma_slow: int | None = None      # new SMA slow window (typical 100-250)
    mr_lookback: int | None = None   # new MR MA window (typical 3-10)
    mr_threshold_pct: float | None = None    # new MR deviation threshold (0.005-0.05)
    reasoning: str = ""              # quantitative evidence for the change


@dataclass
class PipelineFinding:
    """A pipeline-quality issue the analyst spotted in the monthly self-audit.

    These are NOT strategy ideas. They are infrastructure/risk-config
    findings — drift between hardcoded constants and config values, dead
    risk-management features, missing institutional safeguards, etc.

    The monthly review surfaces them in a dedicated section of the email
    with the severity tag prominent. The operator decides whether/how to
    apply each fix. No auto-apply for these — pipeline changes touch
    safety-critical code and need human review.
    """
    severity: str             # "critical" | "high" | "medium" | "low"
    category: str             # "drift" | "dead_code" | "missing_safeguard" | "industry_norm"
    description: str          # 1-2 sentences: what's wrong
    recommendation: str       # 1-2 sentences: what to do about it


@dataclass
class AnalysisReport:
    """Full output of one AI analyst call."""
    analysis: str                         # qualitative narrative
    proposed_strategy: StrategyProposal | None  # None if no proposal
    proposed_state_changes: StateChangeProposal | None = None  # None if no tuning
    # Infrastructure/risk-config findings from the pipeline self-audit
    # (ANALYST.md §6 step 5c). Empty list when the analyst flagged nothing.
    pipeline_findings: list[PipelineFinding] = field(default_factory=list)


@dataclass
class WeeklyAnalysisReport:
    """Output of one weekly performance-review call.

    The weekly analyst does NOT propose strategies or state changes; its
    only deliverable is a structured deep-dive narrative the operator
    reads Saturday morning. See WEEKLY_ANALYST.md for the structure.
    """
    narrative: str   # markdown body, ~4-6 paragraphs per WEEKLY_ANALYST.md §2



def _summarise_runs(daily_runs: list[dict[str, Any]]) -> str:
    """Condense daily run records into a compact table for the prompt.

    Adds a Daily-Return column (equity[t] vs equity[t-1]) so the analyst
    can spot streaks, regime shifts, and bad/good days at a glance
    without having to compute the deltas in its head.
    """
    if not daily_runs:
        return "No daily runs available for the review period."

    sorted_runs = sorted(daily_runs, key=lambda r: r.get("date", ""))
    lines = [
        "Date       | Equity ($)  | Daily Δ% | Entries | Stops | Top-3 positions",
        "-----------|-------------|----------|---------|-------|----------------",
    ]
    prev_equity: float | None = None
    for run in sorted_runs:
        d = run.get("date", "?")
        er = run.get("execution_report", {})
        equity = er.get("account_equity_before")
        orders = er.get("submitted_orders", [])
        entries = sum(1 for o in orders if o.get("role") == "entry")
        stops = sum(1 for o in orders if o.get("role") == "stop_loss")
        targets: dict[str, float] = run.get("target_weights", {})
        top_names = ", ".join(
            sorted(targets, key=lambda s: -targets[s])[:3]
        ) if targets else "—"
        equity_str = f"{equity:>12,.0f}" if isinstance(equity, (int, float)) else f"{'?':>12}"
        # Daily return column — first row shows "—"; subsequent rows show
        # signed pct vs prior equity. This is a holding-only proxy since
        # equity is BEFORE today's trade, capturing yesterday's P&L.
        if isinstance(equity, (int, float)) and prev_equity and prev_equity > 0:
            ret_pct = (equity / prev_equity - 1.0) * 100
            ret_str = f"{ret_pct:+7.2f}%"
        else:
            ret_str = "      —"
        if isinstance(equity, (int, float)):
            prev_equity = float(equity)
        lines.append(
            f"{d} | {equity_str} | {ret_str} | {entries:>7} | {stops:>5} | {top_names}"
        )

    return "\n".join(lines)


def _render_past_weeklies(
    reports: list[dict[str, Any]],
    *,
    heading: str,
    empty_text: str,
) -> str:
    """Render up to N weekly narratives as a single markdown section.

    Shared between the monthly analyst's user message (where the heading
    frames them as "the weekly analyst's curated observations") and the
    weekly analyst's user message (where the heading frames them as the
    analyst's own past narratives for self-improvement). Same format,
    different framing supplied by the caller.
    """
    if not reports:
        return f"## {heading}\n\n_{empty_text}_\n\n"
    parts = [f"## {heading}", ""]
    for r in reports:
        parts.append(
            f"### Week ending {r.get('week_ending', '?')}\n\n"
            f"{r.get('narrative', '(no narrative recorded)')}"
        )
    return "\n\n".join(parts) + "\n\n"


def _build_user_message(
    daily_runs: list[dict[str, Any]],
    current_state: dict[str, Any],
    grid_search_summary: str,
    ai_strategy_names: list[str],
    recent_weekly_reports: list[dict[str, Any]] | None = None,
    monthly_metrics: dict[str, Any] | None = None,
    pipeline_snapshot: dict[str, Any] | None = None,
    quant_diagnostics: dict[str, Any] | None = None,
) -> str:
    """Compose the full user-turn message for the analyst call."""
    runs_table = _summarise_runs(daily_runs)
    state_json = json.dumps(current_state, indent=2)
    active_ai = (
        ", ".join(ai_strategy_names) if ai_strategy_names else "none yet"
    )

    # Recent weekly narratives — oldest first — give the monthly analyst
    # the weekly analyst's curated observations. Per ANALYST.md §6 step
    # 5a, the monthly analyst BUILDS ON these rather than re-deriving
    # attribution from raw daily data. Watch for "WORTH ESCALATING TO
    # MONTHLY REVIEW" markers from the weekly side.
    weekly_section = _render_past_weeklies(
        recent_weekly_reports or [],
        heading=(
            "Recent weekly reports — written by the WEEKLY analyst "
            "(your narrower self), oldest first"
        ),
        empty_text=(
            "No weekly reports available yet (fresh install or first month). "
            "Reason from raw daily data this time; future months will have "
            "weekly observations to build on."
        ),
    )

    # Monthly statistical view — pre-computed metrics over the full
    # 30-day window that the weekly summaries don't surface (lag-1
    # autocorrelation, day-of-week effects, position persistence, HRP
    # weight drift, streak runs, top-10 movers, raw daily-return series).
    # The analyst is directed (ANALYST.md §6 step 5b) to TRIANGULATE
    # these statistical patterns against the weekly narratives and the
    # raw daily-runs table.
    if monthly_metrics:
        metrics_section = (
            "## Monthly Statistical View — pre-computed over the full window "
            "(see ANALYST.md §6 step 5b: triangulate with weekly narratives + "
            "raw daily table)\n\n"
            f"```json\n{json.dumps(monthly_metrics, indent=2, default=str)}\n```\n\n"
        )
    else:
        metrics_section = ""

    # Pipeline snapshot — hardcoded constants + config values + wiring
    # status, bundled by monthly_review._build_pipeline_snapshot(). The
    # analyst must compare these against each other AND against the
    # industry norms (also in the snapshot) and emit findings under
    # `pipeline_findings`. See ANALYST.md §6 step 5c.
    if pipeline_snapshot:
        pipeline_section = (
            "## Pipeline Self-Audit — hardcoded knobs, config values, and "
            "wiring status (see ANALYST.md §6 step 5c)\n\n"
            "**You MUST review this block** and emit `pipeline_findings` "
            "for anything wrong. Specifically:\n"
            "  - Compare `operator_hard_rules_in_code` vs `config_yaml_values` "
            "    for the SAME risk knob. Any mismatch is a drift finding "
            "    (severity: high or critical).\n"
            "  - Check `wiring_status` for advertised-but-not-wired features. "
            "    A False there is dead code (severity: high).\n"
            "  - Compare `sandbox_gates_in_code` to "
            "    `industry_norms_for_comparison`. Anything looser than "
            "    institutional norms is a finding (severity: medium-high).\n"
            "  - If nothing is wrong, emit `pipeline_findings: []`. Don't "
            "    invent findings to look productive.\n\n"
            f"```json\n{json.dumps(pipeline_snapshot, indent=2, default=str)}\n```\n\n"
        )
    else:
        pipeline_section = ""

    # Quant diagnostics (Phase 4): the factor-attribution / signal-health /
    # regime bundle from monthly_diagnostics.build_quant_diagnostics. This
    # is the numbers substrate for the comprehensive review — it answers
    # "alpha or beta?", "which sleeve's edge has decayed?", and "what
    # regime are we in?" objectively. The analyst must reason across the
    # five pillars using THESE numbers, not vibes.
    if quant_diagnostics:
        diag_section = (
            "## Quant Diagnostics — alpha/beta, signal health, regime "
            "(Phase 4; reason across the 5 pillars using THESE numbers)\n\n"
            "Use this to drive the review:\n"
            "  - **Pillar 3 (alpha vs beta):** `attribution` gives the book's "
            "alpha (intercept) and factor `betas`. If alpha is ~0 and R² high, "
            "the book is just factor exposure — say so. Respect `n_obs`/"
            "`warnings`: a short-history alpha is directional only.\n"
            "  - **Pillar 1 (signal health):** `signal_health` has per-strategy "
            "IC, `decaying` flags, `turnover`, and `regime_ic`. A decayed or "
            "regime-dead sleeve is a candidate for retirement or regime-gating "
            "— escalate it.\n"
            "  - **Pillar 4 (risk/regime):** `regime.current`, the correlation "
            "de-gross signal, and `candidate_regime_policy` (sleeve multipliers "
            "for the current regime, derived from regime-conditional IC). "
            "Evaluate whether to adopt it via `proposed_state_changes.regime_"
            "policy` — it auto-applies behind backtest+shadow+DSR gates.\n"
            "  - **Pillars 2 & 5** stay your standard taxonomy-gap search and "
            "pipeline self-audit.\n\n"
            f"```json\n{json.dumps(quant_diagnostics, indent=2, default=str)}\n```\n\n"
        )
    else:
        diag_section = ""

    return (
        f"## Trading Results — last {len(daily_runs)} trading day(s) "
        "(daily-grain table; spot-check specific days here)\n\n"
        f"{runs_table}\n\n"
        f"{metrics_section}"
        f"## Current EnsembleState\n\n```json\n{state_json}\n```\n\n"
        f"## AI-Generated Strategies Already Active\n\n{active_ai}\n\n"
        f"## Monthly Grid-Search Results\n\n{grid_search_summary}\n\n"
        f"{weekly_section}"
        f"{diag_section}"
        f"{pipeline_section}"
        "---\nPlease analyze the results and propose a new strategy if appropriate. "
        "TRIANGULATE three sources before any proposal: (1) weekly narratives — "
        "curated attribution, (2) raw daily table — granular events, "
        "(3) monthly statistical view — longer-horizon patterns. "
        "If they DISAGREE (e.g., weekly attribution credits NVDA but the "
        "monthly day-of-week breakdown shows Monday underperformance is the "
        "real story), explicitly note the disagreement — that's a high-signal "
        f"finding. If weekly reports flagged anything '{ESCALATION_MARKER}', "
        "address those explicitly in your analysis and proposal decisions. "
        "FINALLY: complete the pipeline self-audit and emit `pipeline_findings` "
        "for any infrastructure issues you spotted."
    )


